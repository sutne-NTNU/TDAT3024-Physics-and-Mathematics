function I = moment_of_inertia(R1, L1, R2, L2, p)
M1 = pi * R1^2 * L1 * p;
M2 = pi * R2^2 * L2 * p;

I_xx = ((M1*R1^2)/4) + ...
	   ((M1*L1^2)/12) + ...
	   ((M2*R2^2)/2);
   
I_yy = (M1*R1^2) + ...
	   ((M2*L2^2)/4) + ...
	   ((M1*R1^2)/2) + ...
	   ((M2*R2^2)/4) + ...
	   ((M2*L2^2)/12);
   
I_zz = (M1*R1^2) + ...
	   ((M2*L2^2)/4) + ...
	   ((M1*R1^2)/4) + ...
	   ((M1*L1^2)/12) + ...
	   ((M2*R2^2)/4) + ...
	   ((M2*L2^2)/12);

I = [[I_xx   0     0 ];
	 [  0  I_yy    0 ];
	 [  0    0   I_zz]];
end